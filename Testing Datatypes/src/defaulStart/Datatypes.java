package defaulStart;

public class Datatypes {

	public static void main(String[] args) {
		
		String Bokstaver = "Hello darkness my old friend!"; //C# String, eclipse uthever ikke :(
		long StortHeltall = 20000; //C# long
		int Heltall = 42; //C# int(32) uten mulighet for 12/64
		double Decimal = 42.42; //C# decimal
		boolean Biner = true; //C# sin bool, uten mulighet for definisjon utenfor true/false (0/1)
		float Flyttall = 1.0F; //C# float
		
		System.out.println(Bokstaver);
		System.out.println(StortHeltall);
		System.out.println(Heltall);
		System.out.println(Decimal);
		System.out.println(Biner);
		System.out.println(Flyttall);
		

	}

}
